    private boolean typecheck(javax.swing.JTextField txf, javax.swing.JLabel lbl, String validStr) {
           String txfText = txf.getText();
    for (int i = 0; i < txfText.length(); i++) {
        if (!Character.isLetterOrDigit(txfText.charAt(i)) && !(validStr.indexOf(txfText.charAt(i)) >= 0)) {
            lbl.setText("Invalid");
            return false;
            }
        }
    return true;
    }
    
