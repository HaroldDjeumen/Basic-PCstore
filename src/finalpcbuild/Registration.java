    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        ClearErrorLabels();
        if (validName() && validSurname() && validGmail()) {
            this.toBack();
            setVisible(false);
            AdvancedInfo ai = new AdvancedInfo();
            ai.toFront();
            ai.setVisible(true);
        }
        }

        private boolean validName() {
            String name = txfNames.getText();
            if (name == null || name.length() <= 5) {
                Overall.setText("Invalid");
                return false;
            }
            return true;
        }

        private boolean validSurname() {
            String surname = txfSurname.getText();
            if (surname == null || surname.length() <= 5) {
                Overall.setText("Invalid");
                return false;
            }
            return true;
        }

        private boolean validGmail() {
            String gmail = txfGmail.getText();
            String regex = "^[\\w!#$%&amp;'*+/=?`{|}~^-]+(?:\\.[\\w!#$%&amp;'*+/=?`{|}~^-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,6}$";
            Pattern pattern = Pattern.compile(regex);
            Matcher match = pattern.matcher(gmail);
            if (!match.matches()) {
                Overall.setText("Invalid");
                return false;
            }
            return true;

    }//GEN-LAST:event_jButton1ActionPerformed

